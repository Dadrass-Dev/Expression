namespace Dadrass.Dev.Expression.Core.Token;

/// <summary>
/// Defines token types for arithmetic operators.
/// </summary>
public enum ArithmeticTokenType {
    /// <summary>Represents the "+" operator.</summary>
    Plus,

    /// <summary>Represents the "-" operator.</summary>
    Minus,

    /// <summary>Represents the "*" operator.</summary>
    Star,

    /// <summary>Represents the "/" operator.</summary>
    Slash
}